export interface Complex {
  re: number;
  im: number;
}

// --- Complex helpers ---

function c(re: number, im: number): Complex {
  return { re, im };
}

function cAdd(a: Complex, b: Complex): Complex {
  return { re: a.re + b.re, im: a.im + b.im };
}


export function cNeg(a: Complex): Complex {
  return { re: -a.re, im: -a.im };
}

function cSub(a: Complex, b: Complex): Complex {
  return { re: a.re - b.re, im: a.im - b.im };
}

export function cMul(a: Complex, b: Complex): Complex {
  return {
    re: a.re * b.re - a.im * b.im,
    im: a.re * b.im + a.im * b.re,
  };
}

function cDiv(a: Complex, b: Complex): Complex {
  const denom = b.re * b.re + b.im * b.im;
  return {
    re: (a.re * b.re + a.im * b.im) / denom,
    im: (a.im * b.re - a.re * b.im) / denom,
  };
}

function cConj(a: Complex): Complex {
  return { re: a.re, im: -a.im };
}

export function cScale(a: Complex, s: number): Complex {
  return { re: a.re * s, im: a.im * s };
}

// Split a polynomial in FFT representation.
export function splitFft(fFft: Complex[]): [Complex[], Complex[]] {
  const n = fFft.length;
  // For the 1024-size version, just use the global phi table.
  const w = phi2048Roots;

  const half = n >> 1;
  const f0Fft = new Array<Complex>(half);
  const f1Fft = new Array<Complex>(half);

  for (let i = 0; i < half; i++) {
    const a = fFft[2 * i];
    const b = fFft[2 * i + 1];

    // f0_fft[i] = 0.5 * (f_fft[2i] + f_fft[2i+1])
    f0Fft[i] = cScale(cAdd(a, b), 0.5);

    // f1_fft[i] = 0.5 * (f_fft[2i] - f_fft[2i+1]) * w[2i].conjugate()
    const diff = cSub(a, b);
    const tw = cConj(w[2 * i]);
    f1Fft[i] = cMul(cScale(diff, 0.5), tw);
  }

  return [f0Fft, f1Fft];
}

// Merge two FFT polynomials into one.
export function mergeFft(fListFft: [Complex[], Complex[]]): Complex[] {
  const [f0Fft, f1Fft] = fListFft;
  const n = f0Fft.length * 2;
  const w = phi2048Roots;

  const fFft = new Array<Complex>(n);

  for (let i = 0; i < n / 2; i++) {
    const tw = w[2 * i];

    // f_fft[2i]   = f0[i] + w[2i] * f1[i]
    // f_fft[2i+1] = f0[i] - w[2i] * f1[i]
    const t = cMul(tw, f1Fft[i]);
    fFft[2 * i] = cAdd(f0Fft[i], t);
    fFft[2 * i + 1] = cSub(f0Fft[i], t);
  }

  return fFft;
}

// FFT: input coefficients (number[]), output Complex[] (FFT format)
export function fft(f: number[]): Complex[] {
  const n = f.length;

  if (n > 2) {
    const [f0, f1] = split(f);      // from common.ts
    const f0Fft = fft(f0);
    const f1Fft = fft(f1);
    return mergeFft([f0Fft, f1Fft]);
  } else {
    // Base case n == 2
    const out = new Array<Complex>(2);
    // f_fft[0] = f[0] + i f[1]
    // f_fft[1] = f[0] - i f[1]
    out[0] = c(f[0], f[1]);
    out[1] = c(f[0], -f[1]);
    return out;
  }
}

// iFFT: input Complex[] (FFT), output coefficients (number[])
export function ifft(fFft: Complex[]): number[] {
  const n = fFft.length;

  if (n > 2) {
    const [f0Fft, f1Fft] = splitFft(fFft);
    const f0 = ifft(f0Fft);
    const f1 = ifft(f1Fft);
    return merge([f0, f1]);    // from common.ts
  } else {
    // Base case n == 2
    const f = new Array<number>(2);
    // In the Python code:
    // f[0] = f_fft[0].real
    // f[1] = f_fft[0].imag
    f[0] = fFft[0].re;
    f[1] = fFft[0].im;
    return f;
  }
}

// ----- Coefficient representation -----

export function add(f: number[], g: number[]): number[] {
  if (f.length !== g.length) {
    throw new Error("add: polynomials must have same length");
  }
  const n = f.length;
  const out = new Array<number>(n);
  for (let i = 0; i < n; i++) {
    out[i] = f[i] + g[i];
  }
  return out;
}

export function neg(f: number[]): number[] {
  const n = f.length;
  const out = new Array<number>(n);
  for (let i = 0; i < n; i++) {
    out[i] = -f[i];
  }
  return out;
}

export function sub(f: number[], g: number[]): number[] {
  return add(f, neg(g));
}

// mul(f, g) = ifft( fft(f) * fft(g) )
export function mul(f: number[], g: number[]): number[] {
  const fFft = fft(f);
  const gFft = fft(g);
  return ifft(mulFft(fFft, gFft));
}

// div(f, g) = ifft( fft(f) / fft(g) )
export function div(f: number[], g: number[]): number[] {
  const fFft = fft(f);
  const gFft = fft(g);
  return ifft(divFft(fFft, gFft));
}

// adjoint: ifft(adj_fft(fft(f)))
export function adj(f: number[]): number[] {
  const fFft = fft(f);
  return ifft(adjFft(fFft));
}

// ----- FFT representation -----

export function addFft(fFft: Complex[], gFft: Complex[]): Complex[] {
  if (fFft.length !== gFft.length) {
    throw new Error("addFft: length mismatch");
  }
  const n = fFft.length;
  const out = new Array<Complex>(n);
  for (let i = 0; i < n; i++) {
    out[i] = cAdd(fFft[i], gFft[i]);
  }
  return out;
}

export function subFft(fFft: Complex[], gFft: Complex[]): Complex[] {
  if (fFft.length !== gFft.length) {
    throw new Error("subFft: length mismatch");
  }
  const n = fFft.length;
  const out = new Array<Complex>(n);
  for (let i = 0; i < n; i++) {
    out[i] = cSub(fFft[i], gFft[i]);
  }
  return out;
}

export function mulFft(fFft: Complex[], gFft: Complex[]): Complex[] {
  if (fFft.length !== gFft.length) {
    throw new Error("mulFft: length mismatch");
  }
  const n = fFft.length;
  const out = new Array<Complex>(n);
  for (let i = 0; i < n; i++) {
    out[i] = cMul(fFft[i], gFft[i]);
  }
  return out;
}

export function divFft(fFft: Complex[], gFft: Complex[]): Complex[] {
  if (fFft.length !== gFft.length) {
    throw new Error("divFft: length mismatch");
  }
  const n = fFft.length;
  const out = new Array<Complex>(n);
  for (let i = 0; i < n; i++) {
    out[i] = cDiv(fFft[i], gFft[i]);
  }
  return out;
}

export function adjFft(fFft: Complex[]): Complex[] {
  const n = fFft.length;
  const out = new Array<Complex>(n);
  for (let i = 0; i < n; i++) {
    out[i] = cConj(fFft[i]);
  }
  return out;
}

